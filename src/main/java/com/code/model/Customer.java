package com.code.model;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "customers")
public class Customer {
	@Id
	@Column(name = "id")
	@GeneratedValue(strategy = GenerationType.IDENTITY)
	private int id;

	@Column(name = "name")
	private String name;

	@Column(name = "citizen_identification_number")
	private String citizenIdentificationNumber;

	@Column(name = "phone_number")
	private String phoneNumber;

	@Column(name = "email")
	private String email;

	@Column(name = "address")
	private String address;

	public Customer() {
	}

	public Customer(int id, String name, String citizenIdentificationNumber, String phoneNumber, String email,
			String address) {
		this.id = id;
		this.name = name;
		this.citizenIdentificationNumber = citizenIdentificationNumber;
		this.phoneNumber = phoneNumber;
		this.email = email;
		this.address = address;
	}

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

	public String getCitizenIdentificationNumber() {
		return citizenIdentificationNumber;
	}
	public void setCitizenIdentificationNumber(String citizenIdentificationNumber) {
		this.citizenIdentificationNumber = citizenIdentificationNumber;
	}
	
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}

	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
}